package com.domain;

public class FlatSections {
    private String clientCatalogInformation;

    private String clientGlossaries;

    private String clientSectionList;

    private String catalogCode;

    private Integer id;

    public String getClientCatalogInformation() {
        return clientCatalogInformation;
    }

    public void setClientCatalogInformation(String clientCatalogInformation) {
        this.clientCatalogInformation = clientCatalogInformation;
    }

    public String getClientGlossaries() {
        return clientGlossaries;
    }

    public void setClientGlossaries(String clientGlossaries) {
        this.clientGlossaries = clientGlossaries;
    }

    public String getClientSectionList() {
        return clientSectionList;
    }

    public void setClientSectionList(String clientSectionList) {
        this.clientSectionList = clientSectionList;
    }

    public String getCatalogCode() {
        return catalogCode;
    }

    public void setCatalogCode(String catalogCode) {
        this.catalogCode = catalogCode;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
}